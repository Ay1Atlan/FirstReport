package day07_ifStatement;

public class Calculator {
public static void main(String[] args) {
	        
	        double x = 9;
	        double y = 9;
	        String equation = "+";
	        
	        
	        if(equation == "+") {
	            System.out.println(x + y);
	        }else if(equation == "-")
	            System.out.println(x - y);
	        else if(equation == "/")
	            System.out.println(x / y);
	        else if(equation== "*")
	            System.out.println(x * y);
	        

	    }

	
}

